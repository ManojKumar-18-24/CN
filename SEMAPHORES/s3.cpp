sem_t *sem;

int main()
{
  sem = sem_open("/r2", 0);
  while(1)
  {
        sem_wait(sem);
        cout<<"started\n";
        sleep(5);
        sem_post(sem);
        cout<<"ended\n";
        sleep(2);
  }
  return 0;
}
